unsigned char reverse8( const unsigned char byte)
{
    unsigned int outByte = byte;
    outByte = (outByte & 0x0F) << 4 | (outByte & 0xF0) >> 4;
    outByte = (outByte & 0x33) << 2 | (outByte & 0xCC) >> 2;
    outByte = (outByte & 0x55) << 1 | (outByte & 0xAA) >> 1;
    return outByte;
}

void test_reverse( const unsigned char given, const unsigned char expected )
{
    const unsigned char out = reverse8( given );
    if( out != expected )
    {
	printf( "FAIL: For 0x%02x ---> 0x%02x != 0x%02x\n", given & 0xff, expected & 0xff, out & 0xff );
    }
}

